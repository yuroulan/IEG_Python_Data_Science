# print() is a python built in fx
# will get help from windows to print the output in the monitor (output device)
# when you call a fx the fx name is followed by ()
# in between the open and close brackets we will have n (any) number of arguments
# arguments are separated using comma

print('\n' "Welcome to Python!" '\n')
# the print function prints all the arguments separated by space character
print("Hello Anati!", '\n' "Nice to meet you.:D", '\n' "Enjoy!" '\n')

# named arguments
# print fx has certain behaviour
# after print, automatically bring to the next line
# value printed with the space
# can modify behaviour using (named arguments)

print(1,2, sep='')
print(10, 11, end=' ')
print(12, 13)

